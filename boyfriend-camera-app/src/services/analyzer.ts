  /** 最近 N 次的平均分（连续好评检测） */
  recentAvg?: number
  /** 连续高分次数（用于连续好评文案） */
  streakCount?: number
  /** 总拍摄次数 */
  totalShoots?: number
  /** 是否为首次拍摄 */
  isFirstPhoto?: boolean
  /** 场景类型（用于场景专属夸奖） */
  sceneType?: 'indoor' | 'outdoor' | 'other' | 'cafe' | 'rooftop_night' | 'camping_campfire' | 'snow' | 'ski_resort'
    | 'cherry_blossom' | 'christmas' | 'gym' | 'zoo' | 'train' | 'ancient_town' | 'lighthouse'
    | 'subway' | 'supermarket' | 'rooftop_party' | 'farm' | 'graffiti' | 'aquarium'
    | 'chapel' | 'market_stall' | 'bakery' | 'carousel' | 'greenhouse' | 'tent_camp' | 'graduation'
    | 'festival_lights' | 'graduation_outdoor' | 'old_town' | 'beach_sunset' | 'rainy_street' | 'morning_run' | 'bookstore'
    | 'mirror' | 'carnival' | 'beach'
    | 'dance_performance' | 'red_autumn_detail' | 'rooftop_daytime' | 'amusement_carnival'
    | 'airport_station' | 'meadow_ranch' | 'subway_escalator'
  /** 上次构图分（构图改善检测） */
  lastCompositionScore?: number
  /** 上次表情分（表情改善检测） */
  lastExpressionScore?: number
  /** 上次光线分（光线改善检测） */
